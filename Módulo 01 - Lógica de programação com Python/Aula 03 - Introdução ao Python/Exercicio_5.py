"""
5. Faça um programa que receba 3 números,
calcule o quadrado de cada um deles e mostre o resultado na tela.
"""

numero1 = int(input("Digite o primeiro número: "))
numero2 = int(input("Digite o segundo número: "))
numero3 = int(input("Digite o terceiro número: "))

resultado1 = numero1 ** 2
resultado2 = numero2 ** 2
resultado3 = numero3 ** 2

print(f"O quadrado do primeiro número é {resultado1}\nO quadrado do segundo número é {resultado2}\nO quadrado do terceiro número é {resultado3}")
