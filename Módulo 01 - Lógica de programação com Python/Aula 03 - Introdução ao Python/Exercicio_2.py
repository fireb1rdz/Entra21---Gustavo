"""
2. Escreva um programa que calcule a soma de 3 números e exiba o resultado.
Os 3 números devem ser informados pelo usuário no momento da execução.
"""

valor1 = int(input("Digite o primeiro valor: "))
valor2 = int(input("Digite o segundo valor: "))
valor3 = int(input("Digite o terceiro valor: "))

resultado = valor1 + valor2 + valor3

print(f"O resultado da soma dos valores {valor1, valor2, valor3} é {resultado}")
